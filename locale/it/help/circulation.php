<a name="barc">
Prestito, etichette con o senza codice a barre</a>:
<br>
OpenBiblio è stato ottimizzato per gestire il prestito mediante l'uso di uno scanner e i 
<a href="../shared/help.php?page=barcodes">codici a barre</a> 
impressi sulla tessera e sulla etichetta del libro.
<br>
Si può comunque effettuare il prestito anche non usando i codici a barre, i tesseratipossono essere trovati poer cognome e OPAC in
<a href="../shared/help.php?page=opacLookup">Ricerca per codice a barre</a> 
si recupera il numero di una copia per orario di uscita e dei movimenti.
<br><br><br>

<a name="late">
Calcolo delle multe per i ritardi, che non sono state comunicati</a>:
<br>
non notificare al personale della biblioteca la consegna con ritardo, secondo le regole della biblioteca e in che maniera il personale opera con
 OpenBiblio, può portare alla lunga ad accumulare multe per il ritardo alla riconsegna ingiuste. Per evitarlo andare alla pagine delle Collezioni
 (Zona amministrazione) e configurare la multa giornaliera per il ritardo in tutte le collezioni a zero.