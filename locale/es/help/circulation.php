<a name="barc">
Circulación (prestamos), etiquetas con o sin códigos de barras</a>:
<br>
OpenBiblio las formas de circulación (prestamos) están diseñados para soportar la circulación eficiente cuando se utiliza un escáner de código de barras y 
<a href="../shared/help.php?page=barcodes">Códigos de Barras</a> 
impreso en las tarjetas de miembros y etiquetas de las copias de volumenes.
<br>
It is also possible to circulate when barcode numbers are not known; members can be searched by last 
name and OPAC-based 
También es posible hacer prestamos cuando el número de código de barras no se conocen, los miembros pueden buscar por último
nombre y OPAC basado en
<a href="../shared/help.php?page=opacLookup">Busquedas por Códigos de Barras</a> 
recupera el número de una copia del código de barras para La hora de salida, de llegada o tiempo.
<br><br><br>

<a name="late">
Calculos de Cargos por demoras, que no se hayan notificado</a>:
<br>
no notificar al personal de la biblioteca en la comprobación de los elementos atrasados y el cálculo de recargos.
Dependiendo de las reglas de la biblioteca y el personal de que manera maneja OpenBiblio esto podría dar lugar a recargo injusto
calculado a una cuenta de los miembros. Para evitar esto, vaya a la página de Colecciones (ficha Administración) y configurar
Tarifa diaria de última hora en todas las colecciones a cero.
