<a name="limi"></a>:Limitaciones de los n&uacute;meros de c&oacute;digo de barras de Openbiblio o EspaBiblio:
<ul>
  <li>Los c&oacute;digos de barras deben ser todos los alfab&eacute;ticos y / o caracteres num&eacute;ricos: a-zA-Z0-9 <br>
  Aunque muchas simbolog&iacute;as de c&oacute;digo de barras (tipo de c&oacute;digos de  barras) pueden codificar caracteres especiales, EspaBiblio no permite  caracteres alfanum&eacute;ricos como  - $% espacios. / +  </li>
  <li>Despu&eacute;s de la presentaci&oacute;n, caracteres alfab&eacute;ticos se convierten en min&uacute;sculas. </li>
  <li>La longitud m&aacute;xima de un c&oacute;digo de barras es de 20 caracteres.</li>
</ul>
Cuando eleiga  una estructura de numeraci&oacute;n tiene que estar  consciente de estas limitaciones. Aseg&uacute;rese tambi&eacute;n que la simbolog&iacute;a de  c&oacute;digo de barras seleccionada por Ud. para la impresi&oacute;n no cause problemas con las limitaciones. Por ejemplo: C&oacute;digo 39 (3 de 9, y no la versi&oacute;n  ampliada) codifica s&oacute;lo letras may&uacute;sculas.<br>
