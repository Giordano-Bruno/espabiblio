<a name="scan">Utilizar un lector de c&oacute;digos de barra :</a>
<br>
Cualquier esc&aacute;ner de c&oacute;digo de barras que emula el teclado cuenta con el  apoyo de OpenBiblio. Esto incluye esc&aacute;neres y esc&aacute;neres USB que se  conectan entre la computadora y su teclado (teclado cu&ntilde;a). <br />
La  mayor&iacute;a de lectores de c&oacute;digo de barras puede programarse  autom&aacute;ticamente para a&ntilde;adir un retorno de carro (mismo que pegarle  ENTER en el teclado) para cada exploraci&oacute;n. De esta manera, OpenBiblio  ser&aacute; capaz de procesar su solicitud lo m&aacute;s pronto que escanea el tema.<br>
<br>

<a name="labe"></a>
Etiquetado con c&oacute;digos de barras hace copias circulaci&oacute;n eficiente,  especialmente al usar los c&oacute;digos de barras real de escaneado de c&oacute;digo  de barras y un dispositivo.
<ul>
  <li>Las etiquetas que contienen texto y los n&uacute;meros se pueden imprimir  utilizando un informe PDF; pesta&ntilde;a Informes, imprimir etiquetas.</li>
  <li>Efectiva de los c&oacute;digos de barras se puede producir fuera de  OpenBiblio, por ejemplo mediante el uso de un procesador de textos y  una fuente de c&oacute;digo de barras.</li>
</ul>
