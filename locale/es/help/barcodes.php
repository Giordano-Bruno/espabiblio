<a name="memb">Miembros de los c&oacute;digos de Barra </a>:
<br>
Para un flujo de trabajo eficiente, especialmente cuando se  utiliza un esc&aacute;ner de c&oacute;digos de barras, la utilizaci&oacute;n de numeraci&oacute;n  en la estructura con dichos codigos de son formados con ceros a la izquierda . Si el c&oacute;digo de barras de cada uno de los miembros tiene un  n&uacute;mero fijo de d&iacute;gitos, EspaBiblio se ve obligado a mostrar los  Campos de Informaci&oacute;n de la p&aacute;gina despu&eacute;s de la digitalizaci&oacute;n o  de escribir correctamente un miembro de c&oacute;digos de barras. Esto permite el posterior escaneo de los c&oacute;digos, para comprobar la copia de  los temas. <br />
<br>
<br>
<a name="copy" id="copy">Copia de los c&oacute;digos de barra </a>: <br>

Si su biblioteca cuenta ya con c&oacute;digos de barras a la etiqueta copias,  aseg&uacute;rese de que la estructura de numeraci&oacute;n no entre en conflicto con  las <a href="#limi">limitaciones de los c&oacute;digos de barras EspaBiblio.</a><br>
Si su biblioteca asigna no asigna un &uacute;nico n&uacute;mero de copias para decidir sobre una adecuada estructura de numeraci&oacute;n o el uso<a href="../shared/help.php?page=biblioCopyEdit#auto"> Barcode Number - Autogenerate</a>.
Esto tiene una utilidad propiedad al cambiar de una simple tarjeta de archivo: 
<a href="../shared/help.php?page=biblioCopyEdit#seri">Copiar n&uacute;meros de serie integrados en los n&uacute;meros de c&oacute;digo de barras</a> Facilitar copia para entrar la informaci&oacute;n de una simple tarjeta de  archivo de un &uacute;nico n&uacute;mero cuando no han sido asignadas, s&oacute;lo los  n&uacute;meros de serie de varias copias de un t&iacute;tulo.<br>
