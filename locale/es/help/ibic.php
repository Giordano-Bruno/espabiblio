<H3>1 NATURALEZA DE LA CLASIFICACIÓN</H3>
<P>El sistema IBIC se basa en unos 2.600 códigos de materias, agrupados jerárquicamente en 18 categorías (Artes, Lengua, Literatura y estudios literarios, Ficción y temas afines, Derecho, Medicina...). Del mismo modo, propone 900 códigos denominados ‘calificadores’ para indicar, si se desea, la localización geográfica, las lenguas de la obra, los períodos históricos, los fines didácticos y la edad de interés.</P>
<P>El sistema fue desarrollado en el Reino Unido por BIC (Book Industry Communication – http://www.bic.org.uk) para atender a una demanda compartida por el sector comercial del libro. En 1998 se hizo pública la primera versión del estándar, en cuya elaboración participaron representantes destacados del sector. La última versión (v 2.1), que ha sido la referencia para su adaptación a España, se hizo pública en diciembre del 2010. En octubre de 2011, la clasificación BIC pasa a denominarse IBIC para recoger las adaptaciones del sistema a diferentes mercados como los de Italia, España, Alemania, Noruega, Suecia, Portugal, etc.</P>
<P>En España, la elección del sistema de clasificación de materias IBIC para el mercado del libro ha sido fruto de un acuerdo entre representantes de todo el sector comercial (editoriales, distribuidoras, librerías y Agencia del ISBN), coordinados por la Federación de Gremios de Editores de España por medio de DILVE.</P>
<P>En estos momentos se está llevando a cabo un análisis más detallado del ámbito jurídico y del científico-técnico, con la colaboración de algunas de las editoriales y librerías más significativas así como en coordinación con el Comité Internacional IBIC.</P>
